            try{
                conn = DBconnection.jdbcConnect();
                
                String update = "Update tbl_rentalrate set Rent_id = ' "+auto_rentid()+" ', Reint_Type = ' "+vType+" ', Rental_Daily =   ' "+Daily+" ', Rental_Weekly = ' "+Weekly+" ', Rental_Monthly = ' "+Monthly+" '  ";
                pst = conn.prepareStatement(update);
                pst.execute();
                
                JOptionPane.showMessageDialog(null,"Update");
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e);
            }
            
