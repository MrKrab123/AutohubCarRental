    private void tbl_VehicleMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_VehicleMousePressed
if (SwingUtilities.isRightMouseButton(evt)){
            
            // get the coordinates of the mouse click
            Point p = evt.getPoint();
            
            // get the row index  that contains  that coordinate
            int rowNumber = tbl_Vehicle.rowAtPoint(p);
            
            // get the ListSelectionModel of the JTable
            ListSelectionModel model   =     tbl_Vehicle.getSelectionModel();
            
            // set the selected interval of rows . using  the rowNumber
            // variable for the beginning and end  selects only that one row
            model.setSelectionInterval(rowNumber, rowNumber);

            
            jPopupMenu1.show(tbl_Vehicle, evt.getX(), evt.getY());
        }  

            
            
    }//GEN-LAST:event_tbl_VehicleMousePressed

